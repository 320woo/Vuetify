<template>
  <v-app class="login">
    <v-layout align-center
              justify-center
              :style="{ backgroundImage: `url('${loginBackgroundImage}')`,
                        height: '100%',
                        backgroundPosition: 'center',
                        backgroundRepeat: 'no-repeat',
                        backgroundSize: 'cover' }">
      <v-layout column
                justify-center
                align-center>
        <v-card class="login-card pa-4"
                height="440"
                :color="'rgb(44, 53, 87)'">
          <v-card-title primary-title
                        class="custom-title-center">
            <div class="login-title">드론을 활용한</div>
            <div class="login-title login-title-large">정밀농업 플랫폼</div>
          </v-card-title>
          <v-form>
            <v-text-field prepend-inner-icon="person"
                          box
                          :background-color="'rgb(26, 32, 55)'"
                          color="loginform"
                          v-model="id"
                          name="ID"
                          label="ID"
                          :rules="[rules.required]">
            </v-text-field>
            <v-text-field prepend-inner-icon="lock"
                          box
                          :background-color="'rgb(26, 32, 55)'"
                          color="loginform"
                          :append-icon="visiblePassword ? 'visibility_off' : 'visibility'"
                          v-model="password"
                          @keydown.enter="onClickLogin(id, password)"
                          name="Password"
                          label="Password"
                          @click:append="visiblePassword = !visiblePassword"
                          :type="visiblePassword ? 'text' : 'password'">
            </v-text-field>
            <v-card-actions>
              <v-layout column
                        align-center
                        justify-start
                        :style="{height: '138px', minHeight: '138px'}">
                <v-btn class="login-button white--text"
                       @click="onClickLogin(id, password)"
                       :disabled="id === ''"
                       primary
                       large
                       block>Login
                </v-btn>
                <v-img :src="require('@/assets/login_divider.png')" width=100% height=1px max-width=400px
                       :style="{marginTop: '16px'}"
                       max-height="1px" contain/>
                <!-- <router-link tag="div" to="/selectAccountType" class="create-account">
                  회원가입
                </router-link> -->
              </v-layout>
            </v-card-actions>
          </v-form>
        </v-card>
      </v-layout>
    </v-layout>
  </v-app>
</template>

<script>
  import authAPI from '@/utils/api/authAPI'
  import ConfirmModal from '@/components/shared-components/ConfirmModal'
  import { mapActions } from 'vuex'

  export default {
    theme: {},
    data: () => {
      return {
        id: '',
        password: '',
        visiblePassword: false,
        isActiveLoginButton: false,
        rules: {
          required: value => !!value || '필수 입력'
        }
      }
    },
    computed: {
      loginBackgroundImage() {
        return require('@/assets/login_background_img.png')
      }
    },
    methods: {
      ...mapActions([
        'openGlobalModal',
        'closeGlobalModal'
      ]),
      async onClickLogin(id, password) {
        const data = await authAPI.login(id.trim(), password.trim())
        if ('account_id' in data) {
          await this.$session.start()
          // await this.$session.set('jwt', data.accessToken)
          await this.$session.set('account_id', data.account_id)
          await this.$session.set('user_type', data.user_type)
          await this.$session.set('solar', data.account_id == 'solar')

          console.log(this.$session.get('account_id'), this.$session.get('user_type'))
          // this.$http.defaults.headers.common['Authorization'] = await this.$session.get('jwt')

          this._goHome()
        } else {
          console.log(data)
          this.openGlobalModal({
            modalTitle: '로그인 오류',
            modalContent: data.message
          })
        }
      },
      _goHome() {
        console.log(this.$router.currentRoute.params)
        // return this.$router.replace(this.$route.query.redirect || '/')
        if (this.$route.query.redirect) {
          console.log('redirect YES!!')
          return this.$router.replace({
            path: this.$session.get('solar') ? '/project' : this.$route.query.redirect,
            query: Object.assign(this.$router.currentRoute.params, {})
          })
        } else {
          if (this.$session.get('solar')) {
            console.log('go home for solar')
            return this.$router.replace('/project')
          }

          let page = ''
          switch (this.$session.get('user_type')) {
            case 'admin':
              page = '/home'
              break
            case 'provider':
              page = '/providerHome'
              break
            case 'consumer':
              page = '/userHome'
              break
            default:
              page = '/userHome'
              break
          }
          return this.$router.replace(page)
        }
        // return this.$router.replace(this.$route.query.redirect || '/')
      }
    },
    components: {
      'ConfirmModal': ConfirmModal
    }
  }
</script>

